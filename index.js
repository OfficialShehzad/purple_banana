var bananaContainer = document.getElementById("banana-container")
var banana = document.querySelector("svg")

var getRandomNumber = () => {
    return  Math.ceil(Math.random() * colors.length)
}

// var score = 0;
// var scoreDisplay = document.querySelector(".score")

document.addEventListener("click", (e) => {

    if(
        e.target.classList.value.includes("how-to-play") ||
        e.target.classList.value.includes("close")
    ) return

    // score++

    banana.classList.add('banana-animation')
    setTimeout(() => {
        banana.classList.remove('banana-animation')
    }, 750);


    var randomNumber = getRandomNumber()
    var bananaColor = colors[randomNumber]
    var bgColor = backgroundColors[randomNumber]

    bananaContainer.style.backgroundColor = bgColor
    banana.style.fill = bananaColor

    if (checkWin(bananaColor)) {
        // console.log(score);
        window.location.href = "youwin.html"
    }

})

var checkWin = (color) => {
    if (color == "rgb(128, 0, 128)") {
        return true
    }
    return false
}

